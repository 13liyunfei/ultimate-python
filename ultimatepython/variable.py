def main():
    # There are a couple different literal types
    a = 1
    b = 2.0
    c = True
    d = "hello"

    print(a, type(a))  # <class 'int'>
    print(b, type(b))  # <class 'float'>
    print(c, type(c))  # <class 'bool'>
    print(d, type(d))  # <class 'str'>


if __name__ == "__main__":
    main()
